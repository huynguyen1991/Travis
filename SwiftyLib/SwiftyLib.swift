import Foundation

public final class SwiftyLib {

    let name = "SwiftyLib"
    public func add(value1: Int, value2: Int) -> Int {
        return value1 + value2
    }
    public func sub(value1: Int, value2: Int) -> Int {
        return value1 - value2
    }
}
